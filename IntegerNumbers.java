package com.hundred_code_partices;

public class IntegerNumbers {
	public static void displayIntegerNumbers() {
        int n = 10; //declaring a local variable
        /* Using for loop to repeat instruction execution */
        for (int i = 0; i <= n; i++) {
            System.out.println(i);
        }
    }

    public static void main(String[] args) {
        displayIntegerNumbers();
    }
}
